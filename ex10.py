tabby_cat = "\tI'm tabbed in." #\t instructs to tab in
persian_cat = "I'm split\non a line." #\n instructs to move to next line (like hitting return)
backslash_cat = "I'm \\ a \\ cat." #\\ inputs a \ - escape function from the first \ -- twice in this case

fat_cat = """
I'll do a list:
\t * Cat food
\t * Fishies
\t * Catnip\n\t * Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
